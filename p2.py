# 변수명 앞에 아무것도 없다
# 문장의 끝에 아무것도 없다
# 파이썬의 모든 것은 객체다 => 참조(레퍼런스) 관리
# 열을 잘 맞추어야 한다.
a = 1
print( a, type(a) )
a = 1.1
print( a, type(a) )
# 이미 정해저 있는 내장함수들을 변수명으로 사용하면 오동작
# 문자열을 만드는 함수 str()
print( str(a), type(str(a)) )
str = 2
print( str )
print( str(a) )
